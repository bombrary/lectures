#include <stdio.h>
#include <math.h>

int main(void)
{
	int i;

	for(int i = 1; i <= 999; i++) {
		int n = i;
		int sum = 0;
		while(n) { 
			sum += n % 10; 
			n /= 10;
		}
		printf("%d,%f\n", i, (double)i / sum);
	}

	return 0;
}
