/*(要検証)各桁の和を足した値を表示*/
#include <stdio.h>

int main(void) {
    int N, sum = 0;
    
    scanf("%d", &N);

    while (N != 0) {
        sum += N % 10;
        N /= 10;
    }
    printf("%d\n", sum);

    return 0;
}

